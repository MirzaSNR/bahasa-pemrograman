// latihan function pada TypeScript
// untuk menambah pemahaman Anda, kerjakan latihan berikut ini:

// function declaration 
function perkalian(a, b) {
    return a * b;
};

// function expression
const pengurangan = function (x, y) {
    return x - y;
};

// function Arrow
const pembagian = (x, y) => x / y;




